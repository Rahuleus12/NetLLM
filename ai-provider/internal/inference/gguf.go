// Benchmark runs a benchmark on the loaded model
func (r *GGUFRuntime) Benchmark(ctx context.Context, iterations int) (*BenchmarkResult, error) {
	r.mu.RLock()
	defer r.mu.RUnlock()

	if !r.loaded {
		return nil, fmt.Errorf("model not loaded")
	}

	if iterations <= 0 {
		iterations = 10
	}

	result := &BenchmarkResult{
		Iterations: iterations,
		Timings:    make([]time.Duration, iterations),
	}

	// Run benchmark iterations
	for i := 0; i < iterations; i++ {
		start := time.Now()

		// Simulate inference
		time.Sleep(100 * time.Millisecond)

		result.Timings[i] = time.Since(start)
	}

	// Calculate statistics
	result.CalculateStats()

	return result, nil
}

// BenchmarkResult represents the result of a benchmark run
type BenchmarkResult struct {
	Iterations    int              `json:"iterations"`
	Timings       []time.Duration  `json:"timings"`
	AverageTime   time.Duration    `json:"average_time"`
	MinTime       time.Duration    `json:"min_time"`
	MaxTime       time.Duration    `json:"max_time"`
	TokensPerSec  float64          `json:"tokens_per_sec"`
}

// CalculateStats calculates benchmark statistics
func (r *BenchmarkResult) CalculateStats() {
	if len(r.Timings) == 0 {
		return
	}

	var total time.Duration
	r.MinTime = r.Timings[0]
	r.MaxTime = r.Timings[0]

	for _, timing := range r.Timings {
		total += timing
		if timing < r.MinTime {
			r.MinTime = timing
		}
		if timing > r.MaxTime {
			r.MaxTime = timing
		}
	}

	r.AverageTime = total / time.Duration(len(r.Timings))

	// Estimate tokens per second (assuming 100 tokens per iteration)
	if r.AverageTime > 0 {
		r.TokensPerSec = 100.0 / r.AverageTime.Seconds()
	}
}
